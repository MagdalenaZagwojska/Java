public class ElseIf {
    public static void main(String[] args) {
        int number = 0;
        if (number > 0) {
            System.out.println("Wartość większa od 0");
        } else if (number < 0) {
            System.out.println("Wartość mniejsza od 0");
        } else {
            System.out.println("Wartoć równa się 0");
        }

    }
}
